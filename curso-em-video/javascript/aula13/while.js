//Estrutura de repetição com teste lógico no início

var c = 1 //contador
while (c <= 6) { //teste lógico: "enquanto (c for menor ou igual a 6)"
    console.log(`Passo ${c}`) //"ação do repetidor será executada"
    c++ // neste caso está se adicionando +1 ao valor da variável c (c = c+1).
} //Quando c for maior que 6, o loop acaba